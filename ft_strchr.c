#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	char *s2;

	s2 = (char *)s;
	c = (char)c;
	if (c == '\0')
	{
		while (*s2)
			s2++;
		return (s2);
	}
	while (*s2)
	{
		if (c == *s2)
			return (s2);
		s2++;
	}
	return (NULL);
}
